@app.get("/api/admin/stats")
def get_admin_stats(db: sqlite3.Connection = Depends(get_db)):
    ratings = calculate_ratings_list(db)
    
    # 1. Сводная статистика
    total_students = db.execute("SELECT COUNT(*) FROM student_votes").fetchone()[0]
    total_deans = db.execute("SELECT COUNT(*) FROM expert_votes WHERE role_type='dean'").fetchone()[0]
    total_hods = db.execute("SELECT COUNT(*) FROM expert_votes WHERE role_type='hod'").fetchone()[0]

    today_students = db.execute("SELECT COUNT(*) FROM student_votes WHERE date(timestamp) = date('now', 'localtime')").fetchone()[0]
    today_deans = db.execute("SELECT COUNT(*) FROM expert_votes WHERE role_type='dean' AND date(timestamp) = date('now', 'localtime')").fetchone()[0]
    today_hods = db.execute("SELECT COUNT(*) FROM expert_votes WHERE role_type='hod' AND date(timestamp) = date('now', 'localtime')").fetchone()[0]

    # Вспомогательная функция для расчета нарастающего итога по часовым интервалам суток
    def get_cumulative_trend(table_name, date_sql, extra_filter=""):
        intervals = [
            ("00:00:00", "12:00:00"),
            ("12:00:01", "15:00:00"),
            ("15:00:01", "18:00:00"),
            ("18:00:01", "23:59:59")
        ]
        cumulative = []
        total = 0
        for start, end in intervals:
            q = f"""
                SELECT COUNT(*) FROM {table_name}
                WHERE date(timestamp) = {date_sql}
                AND time(timestamp) >= '{start}' AND time(timestamp) <= '{end}'
                {extra_filter}
            """
            count = db.execute(q).fetchone()[0]
            total += count
            cumulative.append(total)
        return cumulative

    # Расчет кумулятивных кривых (4 точки нарастающим итогом за сутки)
    students_yesterday_curve = get_cumulative_trend("student_votes", "date('now', '-1 day', 'localtime')")
    students_today_curve = get_cumulative_trend("student_votes", "date('now', 'localtime')")

    deans_yesterday_curve = get_cumulative_trend("expert_votes", "date('now', '-1 day', 'localtime')", "AND role_type='dean'")
    deans_today_curve = get_cumulative_trend("expert_votes", "date('now', 'localtime')", "AND role_type='dean'")

    hods_yesterday_curve = get_cumulative_trend("expert_votes", "date('now', '-1 day', 'localtime')", "AND role_type='hod'")
    hods_today_curve = get_cumulative_trend("expert_votes", "date('now', 'localtime')", "AND role_type='hod'")

    # 4. Лидеры-институты по количеству пришедших голосов за сегодня (ВКЛЮЧАЯ ТЕ, У КОГО 0 ГОЛОСОВ)
    # Извлекаем все уникальные институты из БД
    all_insts_rows = db.execute("SELECT DISTINCT institute FROM specialties").fetchall()
    all_institutes = [r["institute"] for r in all_insts_rows if r["institute"] and r["institute"] != '-']

    # Получаем число голосов за сегодня для активных институтов
    inst_today_query = """
        SELECT s.institute, COUNT(*) as vote_count
        FROM student_votes sv
        JOIN specialties s ON sv.specialty_id = s.id
        WHERE date(sv.timestamp) = date('now', 'localtime')
        GROUP BY s.institute
    """
    today_votes_map = {r["institute"]: r["vote_count"] for r in db.execute(inst_today_query).fetchall()}

    # Формируем полный список институтов с учетом нулей и сортируем по убыванию активности
    inst_today = []
    for inst in all_institutes:
        inst_today.append({
            "institute": inst,
            "vote_count": today_votes_map.get(inst, 0)
        })
    inst_today.sort(key=lambda x: x["vote_count"], reverse=True)

    return {
        "stats": {
            "students_voted": total_students,
            "students_today": today_students,
            "students_yesterday_curve": students_yesterday_curve,
            "students_today_curve": students_today_curve,

            "deans_voted": total_deans,
            "deans_today": today_deans,
            "deans_yesterday_curve": deans_yesterday_curve,
            "deans_today_curve": deans_today_curve,

            "hods_voted": total_hods,
            "hods_today": today_hods,
            "hods_yesterday_curve": hods_yesterday_curve,
            "hods_today_curve": today_hods,
        },
        "inst_today": inst_today,
        "ratings": ratings
    }

@app.get("/api/admin/export/csv")
def export_csv(db: sqlite3.Connection = Depends(get_db), x_password: str = Header(None)):
    # Защита выгрузки аналитики
    verify_admin_auth(x_password)
    ratings = calculate_ratings_list(db)
    
    # Хелперы для корректного форматирования под Excel (с русской локалью)
    def to_excel_text(val):
        # Оборачиваем коды в формулу = "значение", чтобы Excel читал их строго как текст 
        # и не превращал коды вроде 07.02.01 в даты (7 февраля)
        if val is None:
            return ""
        return f'="{val}"'

    def to_excel_float(val):
        if val is None:
            return ""
        if isinstance(val, (int, float)):
            # Заменяем точку на запятую, так как при разделителе ";" Excel в русской локали 
            # ожидает запятую в качестве десятичного разделителя.
            # Это убирает превращение 1.25 -> янв.25 и 1.01 -> 45658,00
            return str(val).replace('.', ',')
        return val

    output = StringIO()
    output.write('\ufeff')  # BOM для корректной кодировки UTF-8 в Excel
    
    writer = csv.writer(output, delimiter=';')
    writer.writerow([
        "Код", "Направление подготовки", "Профиль", "Институт", 
        "Руководитель", "Студентов проголосовало", "K_сроки", "Среднее студентов", 
        "R_A (Руководитель)", "R_Б (Наставник)", "R_В (Администратор)"
    ])
    
    for r in ratings:
        writer.writerow([
            to_excel_text(r["code"]), 
            r["spec_name"], 
            r["profile"], 
            r["institute"],
            r["supervisor_name"], 
            r["voters_count"], 
            r["k_sroki"], 
            to_excel_float(r["student_avg"]),
            to_excel_float(r["r_a"]), 
            to_excel_float(r["r_b"]), 
            to_excel_float(r["r_v"])
        ])
        
    output.seek(0)
    return StreamingResponse(
        iter([output.getvalue()]),
        media_type="text/csv",
        headers={"Content-Disposition": "attachment; filename=opop_ratings.csv"}
    )

